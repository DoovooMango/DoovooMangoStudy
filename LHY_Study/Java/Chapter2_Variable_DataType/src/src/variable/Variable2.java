package src.variable;

public class Variable2 {                        /// 변수 초기화
    public static void main(String[] args) {
        int level = 10;	                        // level 변수 선언과 동시에 값을 대입(변수 초기화)
                                                // 초기화 : 변수에 처음 값을 대입해 주는 것
        System.out.println(level);

        // 변수 초기화의 시점은?? 변수를 사용할 때마다 다르다.
        // 변수 선언과 초기화를 동시에 할 수도 있고
        // 변수를 선언한 후에 대입할 값이 정해지는 때에 초기화할 수도 있다.

    }
}