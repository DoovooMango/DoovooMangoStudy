package src.character;

public class Character2 {                           // 유니코드를 활용한 문자형 연습

    public static void main(String[] args) {

        char char1 = '한';                           // '한' 문자열
        char char2 = '\uD55C';                      // '한' 이라는 글자의 유니코드값

        System.out.println(char1);
        System.out.println(char2);

    }
}
