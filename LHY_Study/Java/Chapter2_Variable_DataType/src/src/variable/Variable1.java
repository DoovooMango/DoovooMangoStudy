package src.variable;

public class Variable1 {                            /// 변수 선언
    public static void main(String[] args) {        // main 은 자바 프로그램의 첫 시작

        //int : 자료형
        //level : 변수 이름
        int level;                  // 정수형 변수 level을 선언
        level = 10;                 // level 변수에 값 10을 대입
        System.out.println(level);  // level값 출력

    }
}